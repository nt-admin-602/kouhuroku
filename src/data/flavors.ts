export const FLAVOR_ICON_MAP: Record<string, string> = {
  apple:      '🍎',
  grape:      '🍇',
  berry:      '🫐',
  mint:       '🌿',
  lemon:      '🍋',
  peach:      '🍑',
  watermelon: '🍉',
  grapefruit: '🍊',
  caramel:    '🍮',
  vanilla:    '🫙',
  cinnamon:   '🌶',
  royaltea:   '🍵',
  cola:       '🥤',
  default:    '✨',
}

export function getFlavorDisplayInfo(flavorId: string): {
  displayName: string
  maker: string
  icon: string
} {
  const master = FLAVOR_MASTERS.find(f => f.id === flavorId)
  if (master) {
    return {
      displayName: master.displayName,
      maker: master.maker,
      icon: FLAVOR_ICON_MAP[master.iconKey] ?? '✨',
    }
  }
  // カスタムフレーバー: ID から表示名を復元
  const name = flavorId
    .replace(/^custom-/, '')
    .split('-')
    .map(p => (p ? p.charAt(0).toUpperCase() + p.slice(1) : ''))
    .join(' ')
  return { displayName: name || flavorId, maker: '', icon: '✨' }
}

